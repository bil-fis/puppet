<h2 id="什么是-puppet" tabindex="-1"><a class="header-anchor" href="#什么是-puppet"><span>什么是 Puppet？</span></a></h2>
<p>Puppet 是一个基于 <strong>WebView2</strong> 的 Windows 桌面应用开发框架，允许开发者使用熟悉的 Web 技术（HTML、CSS、JavaScript）构建功能强大的桌面应用程序。</p>
<h3 id="核心特性" tabindex="-1"><a class="header-anchor" href="#核心特性"><span>核心特性</span></a></h3>
<ul>
<li><strong>Web 技术栈</strong>：使用 HTML、CSS、JavaScript 构建界面</li>
<li><strong>原生系统集成</strong>：提供对 Windows 系统功能的完整访问</li>
<li><strong>事件驱动</strong>：支持 USB 设备、磁盘、窗口等事件监控</li>
<li><strong>安全机制</strong>：内置权限控制和安全验证</li>
<li><strong>独特打包</strong>：支持加密的 PUP 打包格式</li>
</ul>
<h3 id="适用场景" tabindex="-1"><a class="header-anchor" href="#适用场景"><span>适用场景</span></a></h3>
<ul>
<li>需要系统级功能的桌面应用</li>
<li>设备管理工具</li>
<li>系统监控工具</li>
<li>托盘应用</li>
<li>透明窗口应用</li>
<li>快速原型开发</li>
</ul>
<h2 id="技术栈" tabindex="-1"><a class="header-anchor" href="#技术栈"><span>技术栈</span></a></h2>
<p>Puppet 框架基于以下技术构建：</p>
<ul>
<li><strong>.NET 9.0</strong> - 核心框架</li>
<li><strong>Windows Forms</strong> - 桌面 UI 框架</li>
<li><strong>WebView2</strong> - 基于 Edge 的 Web 渲染引擎</li>
<li><strong>COM Interop</strong> - JavaScript 与 C# 的桥接</li>
<li><strong>WMI</strong> - Windows 管理接口，用于设备监控</li>
</ul>
